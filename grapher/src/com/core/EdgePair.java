package com.core;

public class EdgePair {

    // the two edges
    Edge e1, e2;

    /**
     * Constructor.
     * @param e1 first edge
     * @param e2 second edge
     * @author Pier Palamara <pier@cs.columbia.edu>
     */
    public EdgePair(Edge e1, Edge e2) {
        this.e1 = e1;
        this.e2 = e2;
    }
}
